    private void mendatar() {
        if (tbMendatar.getText().trim().toLowerCase().equals("gudeg")) {
            a1.setText("G");
            a2.setText("U");
            a3.setText("D");
            a4d5.setText("E");
            a5.setText("G");
            tbMendatar.setText("");
        } else if (tbMendatar.getText().trim().toLowerCase().equals("rendang")) {
            b1.setText("R");
            b2d2.setText("E");
            b3.setText("N");
            b4.setText("D");
            b5e2.setText("A");
            b6.setText("N");
            b7.setText("G");
            tbMendatar.setText("");
        } else if (tbMendatar.getText().trim().toLowerCase().equals("bubur")) {
            c1e4.setText("B");
            c2.setText("U");
            c3.setText("B");
            c4.setText("U");
            c5.setText("R");
            tbMendatar.setText("");
        } else if (tbMendatar.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Masukkan jawaban Anda");
        } else {
            JOptionPane.showMessageDialog(null, "Jawaban Anda salah");
            tbMendatar.setText("");
        }
        vali_dasi();
    }

    private void menurun() {
        if (tbMenurun.getText().trim().toLowerCase().equals("pempek")) {
            d1.setText("P");
            b2d2.setText("E");
            d3.setText("M");
            d4.setText("P");
            a4d5.setText("E");
            d6.setText("K");
            tbMenurun.setText("");
        } else if (tbMenurun.getText().trim().toLowerCase().equals("sambal")) {
            e1.setText("S");
            b5e2.setText("A");
            e3.setText("M");
            c1e4.setText("B");
            e5.setText("A");
            e6.setText("L");
            tbMenurun.setText("");
        }
        vali_dasi();
    }
    
    private void vali_dasi() {
        if (a1.getText().equals("G") && b1.getText().equals("R") && c2.getText().equals("U")) {
            tbMendatar.setEnabled(false);
            btnMendatar.setEnabled(false);
        }
//        
        if (d1.getText().equals("P") && e1.getText().equals("S")) {
            tbMenurun.setEnabled(false);
            btnMenurun.setEnabled(false);
        }
    }
